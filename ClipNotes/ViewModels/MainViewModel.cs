    [RelayCommand]
    private async Task AddMarkerAsync(MarkerType type)
    {
        if (!IsRecording || _currentSession == null) return;

        try
        {
            var status = await _obs.GetRecordStatusAsync();
            if (status == null) return;

            var marker = new Marker
            {
                Index = Markers.Count + 1,
                Type = type,
                Timestamp = status.Value.duration,
                Timecode = status.Value.timecode.Length > 8
                    ? status.Value.timecode[..8]
                    : status.Value.timecode
            };

            Application.Current?.Dispatcher.Invoke(() =>
            {
                Markers.Add(marker);
                SaveMarkersToSession();
            });
        }
        catch (Exception ex)
        {
            LogSvc.Error("AddMarker failed", ex);
            RecordingStatus = $"{Loc.T("loc_StatusError")}: {ex.Message}";
        }
    }

    [RelayCommand]
    private async Task GenerateAsync()
    {
        if (_currentSession == null) return;
        if (IsProcessing) return;

        if (string.IsNullOrEmpty(_currentSession.VideoFilePath) || !File.Exists(_currentSession.VideoFilePath))
        {
            ProcessingStatus = Loc.T("loc_StatusNeedVideo");
            return;
        }

        // Спросить имя сессии перед генерацией, если оно не было задано при записи
        if (AskSessionName && _sessionNameWasAutoGenerated)
        {
            var input = InputDialog.Show(Loc.T("loc_DlgSessionName"), Loc.T("loc_DlgSessionNamePrompt"), "");
            if (input == null) return; // пользователь отменил
            var userProvidedName = input.Trim();
            if (!string.IsNullOrEmpty(userProvidedName))
            {
                _currentSession.SessionName = userProvidedName;
                SessionName = userProvidedName;
                _sessionNameWasAutoGenerated = false;
            }
        }

        IsProcessing = true;
        ProcessingStatus = Loc.T("loc_StatusProcessing");
        ProcessingProgress = 0;
        _pipelineCts = new CancellationTokenSource();

        try
        {
            SetupToolPaths();
            _currentSession.Markers = Markers.ToList();

            var pipeline = new PipelineService(_ffmpeg, _whisper, _excel);
            pipeline.StatusChanged += s =>
                Application.Current?.Dispatcher.Invoke(() => ProcessingStatus = s);
            pipeline.ProgressChanged += (current, total) =>
                Application.Current?.Dispatcher.Invoke(() =>
                {
                    ProcessingProgress = current;
                    ProcessingTotal = total;
                });

            await pipeline.RunAsync(_currentSession, _pipelineCts.Token);
            CopyToCustomPaths(_currentSession);

            ProcessingStatus = Loc.T("loc_StatusProcessingDone");
        }
        catch (OperationCanceledException)
        {
            ProcessingStatus = Loc.T("loc_StatusCancelled");
        }
        catch (Exception ex)
        {
            LogSvc.Error("Generation pipeline failed", ex);
            var msg = ex.Message.Length > 300 ? ex.Message[..300] + "…" : ex.Message;
            ProcessingStatus = $"{Loc.T("loc_StatusError")}: {msg}";
        }
        finally
        {
            IsProcessing = false;
        }
    }

    [RelayCommand]
    private void CancelGeneration()
    {
        _pipelineCts?.Cancel();
    }

